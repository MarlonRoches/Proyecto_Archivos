    public byte[] abrirImagen(File archivo) throws FileNotFoundException, IOException{
        byte[] bytesFoto = new byte[1024*100];
        entrada = new FileInputStream(archivo);
        entrada.read(bytesFoto);
        return bytesFoto;
    }
